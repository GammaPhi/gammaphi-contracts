def verify(
    inputs: list,
    proof: dict) -> int:
    IC = vk['IC']
    assert len(inputs) + 1 == len(IC), "verifier-bad-input"
    # Compute the linear combination vk_x
    vk_x = P.compute_vk(IC, inputs)

    p = [
        proof['A'],
        vk['vk_alfa_1'],
        vk_x,
        proof['C']
    ]
    q = [
        proof['B'],
        vk['vk_beta_2'],
        vk['vk_gamma_2'],
        vk['vk_delta_2']
    ]
    return P.final_result(p, q)


@export
def verify_proof(
    a: list,
    b: list,
    c: list,
    inputs: list
) -> bool:
    proof = {}
    proof['A'] = (int(a[0]), int(a[1]), int(a[2]), 1)
    proof['B'] = (
        [int(b[0][1]), int(b[0][0])],
        [int(b[1][1]), int(b[1][0])],
        [int(b[2][1]), int(b[2][0])],
        [0, 1]
    )
    proof['C'] = (int(c[0]), int(c[1]), int(c[2]), 1)
    inputs = [int(i) for i in inputs]
    if verify(inputs, proof) == 0:
        return True
    else:
        return False
